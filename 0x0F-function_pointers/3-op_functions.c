/**
 * opp_add - adds 2 numbers
 * @x: First number
 * @y: Second number
 * Return: The result
 */
int op_add(int x, int y)
{
return (x + y);
}

/**
 * op_sub - sub 2 numbers
 * @x: First number
 * @y: Second number
 * Return: The result
 */
int op_sub(int x, int y)
{
return (x - y);
}

/**
 * op_mod - mods 2 numbers
 * @x: First number
 * @y: Second number
 * Return: The result
 */
int op_mod(int x, int y)
{
return (x % y);
}

/**
 * op_mul - multiply 2 numbers
 * @x: First number
 * @y: Second number
 * Return: The result
 */
int op_mul(int x, int y)
{
return (x * y);
}

/**
 * op_div - divides 2 numbers
 * @x: First number
 * @y: Second number
 * Return: The result
 */
int op_div(int x, int y)
{
return (x / y);
}
